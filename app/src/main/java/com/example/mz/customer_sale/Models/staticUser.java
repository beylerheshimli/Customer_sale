public final class staticUser {

    public static  int id_;
    public static String username;
    public static String token;
    public static String name_;

    public static int getId_() {
        return id_;
    }

    public static void setId_(int id_) {
        staticUser.id_ = id_;
    }

    public static String getUsername() {
        return username;
    }

    public static void setUsername(String username) {
        staticUser.username = username;
    }

    public static String getToken() {
        return token;
    }

    public static void setToken(String token) {
        staticUser.token = token;
    }

    public static String getName_() {
        return name_;
    }

    public static void setName_(String name_) {
        staticUser.name_ = name_;
    }
}
